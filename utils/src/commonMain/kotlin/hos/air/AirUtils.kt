/**
 * 空气质量工具类，提供空气质量等级、因子符号、单位等基础支持
 */
object AirUtils {

    /**
     * 返回单位“μg/m³”的字符串表示
     *
     * @return 字符串 "μg/m³"
     */
    fun unitUg(): String {
        return "μg/m³"
    }

    /**
     * 返回单位“mg/m³”的字符串表示
     *
     * @return 字符串 "mg/m³"
     */
    fun unitMg(): String {
        return "mg/m³"
    }
}